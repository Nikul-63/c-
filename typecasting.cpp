/* Type casting :- type casting is a way of changing on expression from one data type to another. */

#include<iostream>
using namespace std;

 int main()
 {
 	int a=40;
 	float b=50.60;
 	
 	cout<<"The value of a is:-"<<(float)a<<endl;
 	cout<<"The value of a is:-"<<float(a)<<endl;
 	
 	cout<<"The value of b is:-"<<(int)b<<endl;
 	cout<<"The value of b is:-"<<int(b)<<endl;
 	return 0;
 }
